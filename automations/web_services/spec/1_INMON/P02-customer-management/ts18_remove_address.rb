    before :all do
      CustomerManagement.add_address(caller_id, customer_id, username, type, street, unit, city, country, province, postal_code)

      fetch_res1 = CustomerManagement.fetch_addresses(caller_id, customer_id, username)
      address_id = fetch_res1.xpath('//address/@id').text

      CustomerManagement.remove_address(caller_id, customer_id, username, address_id)

      fetch_res2 = CustomerManagement.fetch_addresses(caller_id, customer_id, username)
      address_node = fetch_res2.xpath('//address').count
    end

    it 'Check Type: ' do
      expect(fetch_res1.xpath('//address/@type').text).to eq(type)
    end

    it 'Check Unit: ' do
      expect(fetch_res1.xpath('//address/street/@unit').text).to eq(unit)
    end

    it 'Check Street: ' do
      expect(fetch_res1.xpath('//address/street').text).to eq(street)
    end

    it 'Check City: ' do
      expect(fetch_res1.xpath('//address/region/@city').text).to eq(city)
    end

    it 'Check Country: ' do
      expect(fetch_res1.xpath('//address/region/@country').text).to eq(country)
    end

    it 'Check Province: ' do
      expect(fetch_res1.xpath('//address/region/@province').text).to eq(province)
    end

    it 'Check Postal Code: ' do
      expect(fetch_res1.xpath('//address/region/@postal-code').text).to eq(postal_code)
    end

    it 'Verify removeAddress call successfully' do
      expect(address_node).to eq(0)
    end
